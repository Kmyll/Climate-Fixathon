  <h2>1997 - Kyoto Protocol</h2>
  <p><br>
    Unlike the second WCC, the Kyoto Protocol contains concrete and mandatory objectives for the country who signed it. It targets the reduction of the 6 greenhouse gas, including Carbon Dioxide, Methane, Nitrous oxide, Hydrofluorocarbons, Perfluorocarbons, and Sulphur Hexafluoride. <br><br>
    It is a universal protocol in which the objective is for all developed countries to reduce their carbon footprint by 5%. The protocol expired in 2012 and resulted in mixed success.
</p><br>


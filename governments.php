<h2>1992 - The Rio Earth Summit</h2>
<p><br>Also known as the United Nations Conference on Environment and Development (UNCED), it is the first International Earth Summit. Back then around 100 nations were represented. This summit's goal was to address climate and environmental issues while rethinking the global socio-economical development pattern. This summit lead to 3 achievements :</p><br>
<ul>
  <li>Agreement on the Climate Change Convention (which led to the Kyoto Protocol)</li>
  <li>Protect First Nation's land against environmental or cultural degradations</li>
  <li>Signature of the Convention of Biological Diversity</li>
</ul><br>
<p>Another Earth Summit was again held in Rio in 2012 to secure renevable actions taken for sustainable development and adress new challenges regarding evolutions and nowadays habits, lifestyles and technologies.</p><br><br>


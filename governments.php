  <h2>2007 - European Union's Climate & Energy Package</h2>
  <p><br>
    The Energy Package is a set of mandatory and compelling laws and regulations to allow the UE to reach its 2020 climate targets. The package have been legislated in 2009 and contains 3 main objectives: </p><br>
    <ul>
      <li>Cut its Greenhouse emissions by 20%</li>
      <li>Provides 20% of its total energy from renewables sources</li>
      <li>Improves its energy efficiency by 20%.</li>
    </ul>
